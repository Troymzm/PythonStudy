mystr = "hello world and itcast and itheima and Python"
# 1. capitalize() 字符串首字母大写
new_str = mystr.capitalize()
print(new_str)
# 2. title() 字符串中每个单词首字母大写
new_str = mystr.title()
print(new_str)
# 3. upper() 小写转大写
new_str = mystr.upper()
print(new_str)
# 4. lower() 大写转小写
new_str = mystr.lower()
print(new_str)