mystr = 'hello'
print(mystr)
new_str = mystr.ljust(10)  #左对齐
print(new_str)
new_str = mystr.ljust(10,'.')  #左对齐
print(new_str)
new_str = mystr.rjust(10)  #右对齐
print(new_str)
new_str = mystr.rjust(10,'.')  #右对齐
print(new_str)
new_str = mystr.center(10)  #中间对齐
print(new_str)
new_str = mystr.center(10,'.')  #中间对齐
print(new_str)